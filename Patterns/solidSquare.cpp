#include<iostream>
using namespace std;
int main()
{
    int m=4;
    int n=4;
    for (int i = 0; i < m; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cout<<"* ";
        }
        cout<<endl;
    }
    return 0;
}


